def main():
    parser = argparse.ArgumentParser(description="Agile Business -- Universal Launcher")
    group = parser.add_mutually_exclusive_group()
    group.add_argument("--docker", action="store_true", help="Force Docker Compose mode")
    group.add_argument("--local", action="store_true", help="Force local dev mode (Node + Python)")
    group.add_argument("--stop", action="store_true", help="Stop running Docker containers")
    args = parser.parse_args()

    print_banner()

    if args.stop:
        stop_docker()
        return

    if args.docker:
        if not can_docker():
            log("Docker is not running or not installed!", "red")
            log("Install Docker Desktop: https://www.docker.com/products/docker-desktop/", "yellow")
            sys.exit(1)
        start_docker()
        return

    if args.local:
        start_local()
        return

    # Auto-detect
    log("Auto-detecting best launch strategy...\n", "bold")

    has_docker = can_docker()
    has_dc = docker_compose_cmd() is not None
    has_node = cmd_exists("node")
    has_python = find_python() is not None

    log(f"   Docker:         {'YES' if has_docker else 'NO'}", "green" if has_docker else "yellow")
    log(f"   Docker Compose: {'YES' if has_dc else 'NO'}", "green" if has_dc else "yellow")
    log(f"   Node.js:        {'YES' if has_node else 'NO'}", "green" if has_node else "yellow")
    log(f"   Python 3:       {'YES' if has_python else 'NO'}", "green" if has_python else "yellow")

    if has_docker and has_dc:
        log("\n   -> Using Docker Compose (recommended)", "bold")
        start_docker()
    elif has_node and has_python:
        log("\n   -> Using Local Dev Mode (Node.js + Python)", "bold")
        start_local()
    else:
        log("\nCannot start! Need either:", "red")
        log("  a) Docker Desktop  -- https://www.docker.com/products/docker-desktop/", "yellow")
        log("  b) Node.js + Python -- https://nodejs.org/ + https://python.org/", "yellow")
        sys.exit(1)


if __name__ == "__main__":
    main()
