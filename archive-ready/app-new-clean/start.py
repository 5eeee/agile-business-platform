#!/usr/bin/env python3
"""
Agile Business -- Universal Launcher
=====================================
Single-file utility: auto-detects environment, starts the site, opens browser.

Works on: Windows, macOS, Linux
Strategies (in priority order):
  1. Docker Compose  (docker + docker-compose available)
  2. Local Dev Mode  (Node.js + Python available)

Usage:
  python start.py            # auto-detect best strategy
  python start.py --docker   # force Docker mode
  python start.py --local    # force local dev mode
  python start.py --stop     # stop running containers/processes
"""

