# ── Constants ──────────────────────────────────────────────────

ROOT = os.path.dirname(os.path.abspath(__file__))
SERVER_DIR = os.path.join(ROOT, "server")
CLIENT_DIR = os.path.join(ROOT, "client-new")

IS_WIN = platform.system() == "Windows"
IS_MAC = platform.system() == "Darwin"
SUPPORTED_PYTHON_MINORS = (11, 12, 13)

DOCKER_URL = "http://localhost:3000"
DEV_URL = "http://localhost:5173"

# ── Helpers ────────────────────────────────────────────────────

_children = []

def log(msg, color="cyan"):
    colors = {
        "cyan": "\033[96m", "green": "\033[92m", "yellow": "\033[93m",
        "red": "\033[91m", "bold": "\033[1m", "reset": "\033[0m",
    }
    c = colors.get(color, "")
    r = colors["reset"]
    print(f"{c}{msg}{r}")


def log_step(n, msg):
    log(f"\n>> [{n}] {msg}", "green")


def cmd_exists(name):
    return shutil.which(name) is not None


def run(cmd, cwd=None, check=True, capture=False):
    kwargs = dict(cwd=cwd or ROOT, shell=True)
    if capture:
        kwargs["stdout"] = subprocess.PIPE
        kwargs["stderr"] = subprocess.PIPE
        kwargs["text"] = True
    result = subprocess.run(cmd, **kwargs)
    if check and result.returncode != 0:
        log(f"Command failed: {cmd}", "red")
        sys.exit(1)
    return result


def run_bg(cmd, cwd=None):
    kwargs = dict(cwd=cwd or ROOT, shell=True)
    if IS_WIN:
        kwargs["creationflags"] = subprocess.CREATE_NEW_PROCESS_GROUP
    p = subprocess.Popen(cmd, **kwargs)
    _children.append(p)
    return p


def port_open(port, host="127.0.0.1", timeout=2):
    try:
        with socket.create_connection((host, port), timeout=timeout):
            return True
    except (OSError, ConnectionRefusedError):
        return False


def wait_for_port(port, label, max_wait=120):
    log(f"   Waiting for {label} on port {port}...", "yellow")
    start = time.time()
    while time.time() - start < max_wait:
        if port_open(port):
            log(f"   {label} ready!", "green")
            return True
        time.sleep(2)
    log(f"   Timeout waiting for {label} (port {port})", "red")
    return False


def open_browser(url):
    log(f"\n   Opening browser: {url}", "bold")
    time.sleep(1)
    webbrowser.open(url)


def cleanup_handler(signum, frame):
    log("\nShutting down...", "yellow")
    for p in _children:
        try:
            p.terminate()
        except Exception:
            pass
    sys.exit(0)


signal.signal(signal.SIGINT, cleanup_handler)
signal.signal(signal.SIGTERM, cleanup_handler)
if IS_WIN:
    try:
        signal.signal(signal.SIGBREAK, cleanup_handler)
    except (AttributeError, ValueError):
        pass

